class Contact{

    constructor(
        nombre,
        apellidos,
        movil,
        direccion,
        poblacion,
        c_postal,
        provincia,
        email,
        f_nacimiento
    ){
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.movil = movil;
        this.direccion = direccion;
        this.poblacion = poblacion;
        this.c_postal = c_postal;
        this.provincia = provincia;
        this.email = email;
        this.f_nacimiento = f_nacimiento;
    }

}

export default Contact;